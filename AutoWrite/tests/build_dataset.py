_CSV_FIELDS = [f.name for f in fields(AutoWriteSample)]


def export_to_csv(
    samples: list[AutoWriteSample],
    output_path: str,
) -> None:
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow(_CSV_FIELDS)
        for s in samples:
            writer.writerow(astuple(s))
    print(f"[build_dataset] 已导出 {len(samples)} 条样本 → {output_path}")


def print_stats(samples: list[AutoWriteSample]) -> None:
    from collections import Counter
    subtype_counts = Counter(s.subtype for s in samples)
    label_counts = Counter(s.expected_label for s in samples)
    print("\n=== 数据集统计 ===")
    for st, cnt in sorted(subtype_counts.items()):
        print(f"  {st:10s}: {cnt:3d} 条")
    print(f"  {'---':10s}  ---")
    print(f"  {'TOTAL':10s}: {len(samples):3d} 条")
    print(f"\n  attack : {label_counts['attack']:3d}")
    print(f"  benign : {label_counts['benign']:3d}")


